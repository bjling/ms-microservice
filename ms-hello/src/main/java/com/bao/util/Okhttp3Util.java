    public static void main(String args[]) throws IOException {


        OkHttpClient.Builder mBuilder = new OkHttpClient.Builder();
        mBuilder.sslSocketFactory(createSSLSocketFactory(),new TrustAllManager());
        mBuilder.hostnameVerifier(new TrustAllHostnameVerifier());
        OkHttpClient client = mBuilder.build();

        String url = "https://ycc.wywk.cn:8443/ycc/api/store/queryAreaClient?signature=5305AD699E76BCFD996C26730C100C7C&bizData=eyJzdG9yZUNvbW1vbiI6IjAyOTAifQ==&timestamp=1494416675";
        Request request = new Request.Builder()
                .url(url)
                .build();

        Response response = client.newCall(request).execute();
        System.out.println(new String(response.body().bytes()));


    }


